package Objetos;

public class List6Animal {
	
	public String nome;
	public String raca;
	public int idade;
	public boolean latir;
	public boolean quadrupide;
	public boolean sobeArvore;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getRaca() {
		return raca;
	}
	public void setRaca(String raca) {
		this.raca = raca;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public boolean isLatir() {
		return latir;
	}
	public void setLatir(boolean latir) {
		this.latir = latir;
	}
	public boolean isQuadrupide() {
		return quadrupide;
	}
	public void setQuadrupide(boolean quadrupide) {
		this.quadrupide = quadrupide;
	}
	public boolean isSobeArvore() {
		return sobeArvore;
	}
	public void setSobeArvore(boolean sobeArvore) {
		this.sobeArvore = sobeArvore;
	}
	
	
	
	
}
